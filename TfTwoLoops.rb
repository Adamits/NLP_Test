# This is a fake calculation simply used to test the efficiency of 2 different strategies 
# for looping over data that will have calculations be run on it.
# 
# This strategy is a little slower with smaller amounts of words
# But as the content gets larger, runtime increases only slightly
#
class TfTest
	def initialize(content)
		@content = content
	end

	def time_method(method)
	  beginning_time = Time.now
	  self.send(method)
	  end_time = Time.now
	  puts "Time elapsed #{(end_time - beginning_time)*1000} milliseconds"
	end

	def get_tf
		@tf_hash = Hash.new
		# The amount of times a term appears 
		# In the document divided by the total 
		# Amount of tokens in the document.
		@quantity = self.tokenize.length
		@tokens.each do |term|
			if @tf_hash[term]
				@tf_hash[term] += 1
			else
				@tf_hash[term] = 1
			end
		end

		@tf_hash.each do |term, weight|
			@tf_hash[term] = Math.log(@quantity/@tf_hash[term])
		end
		return @tf_hash
	end

	def tokenize
		@tokens = @content.gsub(/[^a-zA-Z0-9 - ' _]/, "")
		@tokens = @tokens.split(' ')
		return @tokens
	end
end

